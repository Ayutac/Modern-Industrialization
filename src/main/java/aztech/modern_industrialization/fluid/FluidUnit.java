package aztech.modern_industrialization.fluid;

/**
 * The basic fluid unit is the bucket.
 * Each bucket is divided in 81 x 16 = 1296 drops.
 * Amounts of fluid are stored in drops.
 */
public class FluidUnit {
    public static final int DROPS_PER_NUGGET = 16;
    public static final int DROPS_PER_INGOT = 144;
    public static final int DROPS_PER_BLOCK = 1296;
    public static final int DROPS_PER_BUCKET = 1296;
}
